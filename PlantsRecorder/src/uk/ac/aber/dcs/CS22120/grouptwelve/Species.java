public class Species {

	private int id;
	private String name;
	private String comment;

	public species() {

	}

	public void setID(int i) {
		id = i;
	}

	public void setName(String n) {
		name = n;
	}

	public void setComment(String c) {
		comment = c;

	}

	public int getID() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getComment() {
		return comment;
	}
}
